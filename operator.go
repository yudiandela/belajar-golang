package main

import "fmt"

func main() {
	// Operator Aritmatika Dasar
	fmt.Println(10 + 1) // Penjumlahan
	fmt.Println(10 - 1) // Pengurangan
	fmt.Println(10 * 2) // Perkalian
	fmt.Println(10 / 2) // Pembagian
	fmt.Println(10 % 3) // Modulo / Sisa Bagi

	// Operator Logika
	fmt.Println(true && true)  // Benar dan Benar
	fmt.Println(true && false) // Benar dan Salah
	fmt.Println(true || true)  // Benar atau Benar
	fmt.Println(true || false) // Benar atau Salah
	fmt.Println(!true)         // Tidak Benar
}
